    class Program {
        private static ConsoleEventDelegate handler;
        private delegate bool ConsoleEventDelegate(int eventType);
        private static NotifyIcon trayIcon = new NotifyIcon(); 

        static void Main(string[] args) {
            trayIt();

            handler = new ConsoleEventDelegate(ConsoleEventCallback);
            SetConsoleCtrlHandler(handler, true);

            while (true) {
                if (GetActiveProcessName() == "TESV") {
                    ShowCursor(false);
                } else {
                    ShowCursor(true);
                }

                Thread.Sleep(2000);
            }
        }

        private static void trayIt() {
            trayIcon.Icon = new sIcon().skyrimIcon();
            trayIcon.Text = "Skyrim Cursor Corrector";
            trayIcon.Visible = true;

            trayIcon.ContextMenu = new ContextMenu();
            trayIcon.ContextMenu.MenuItems.Add("Exit", closeProgram);
            Application.Run();
        }

        private static void closeProgram(object obj, EventArgs e) {
            ShowCursor(true);
            trayIcon.Dispose();
            Environment.Exit(-1);
        }

        private static bool ConsoleEventCallback(int eventType) {
            ShowCursor(true);
            trayIcon.Dispose();
            return false;
        }

        [DllImport("kernel32.dll", SetLastError = true)]
        private static extern bool SetConsoleCtrlHandler(ConsoleEventDelegate callback, bool add);

        [DllImport("user32.dll")]
        private static extern IntPtr GetWindowThreadProcessId(IntPtr hWnd, out uint ProcessId);

        [DllImport("user32.dll")]
        private static extern IntPtr GetForegroundWindow();

        [DllImport("user32.dll")]
        private static extern int ShowCursor(bool bShow);

        private static string GetActiveProcessName() {
            IntPtr hwnd = GetForegroundWindow();
            uint pid;
            GetWindowThreadProcessId(hwnd, out pid);
            return Process.GetProcessById((int)pid).ProcessName;
        }

